namespace Wordroller.Content.Properties.Tables.Widths
{
	/// <summary>
	///     ST_TblWidth
	/// </summary>
	public enum TableWidthUnit
	{
		/// <summary>
		///     Automatically determined width, numeric value is ignored
		/// </summary>
		Auto,

		/// <summary>
		///     Width in twentieths of a point
		/// </summary>
		Dxa,

		/// <summary>
		///     No width, numeric value is ignored
		/// </summary>
		Nil,

		/// <summary>
		///     Width in fiftieths of a percent
		/// </summary>
		Pct
	}
}